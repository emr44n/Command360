'use client'
import { useState } from 'react'
import { Button } from '@/components/ui/button'
import { FileDown, Loader2 } from 'lucide-react'
import { toast } from 'sonner'

interface ExportPDFButtonProps {
  presentationTitle: string
  sessionCode: string
  participantCount: number
  responseCount: number
  sessionDate: string
}

export function ExportPDFButton({
  presentationTitle,
  sessionCode,
  participantCount,
  responseCount,
  sessionDate,
}: ExportPDFButtonProps) {
  const [generating, setGenerating] = useState(false)

  async function handleExport() {
    setGenerating(true)
    try {
      // Dynamic import to keep bundle small
      const { default: jsPDF } = await import('jspdf')

      const doc = new jsPDF({
        orientation: 'portrait',
        unit: 'mm',
        format: 'a4',
      })

      const pageW = doc.internal.pageSize.getWidth()
      const margin = 20
      let y = margin

      // Header
      doc.setFontSize(8)
      doc.setTextColor(150)
      doc.text('COMMAND 360 — SESSION REPORT', margin, y)
      doc.text(new Date().toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' }), pageW - margin, y, { align: 'right' })

      y += 12

      // Title
      doc.setFontSize(22)
      doc.setTextColor(30)
      doc.text(presentationTitle || 'Untitled Deck', margin, y)

      y += 10
      doc.setFontSize(10)
      doc.setTextColor(100)
      doc.text(`Session: ${sessionCode}  |  Date: ${new Date(sessionDate).toLocaleDateString('en-GB', { day: 'numeric', month: 'short', year: 'numeric' })}`, margin, y)

      y += 15

      // Stats boxes
      doc.setFillColor(248, 248, 248)
      const boxW = (pageW - margin * 2 - 10) / 3
      const stats = [
        { label: 'Participants', value: String(participantCount) },
        { label: 'Responses', value: String(responseCount) },
        { label: 'Avg/person', value: participantCount > 0 ? (responseCount / participantCount).toFixed(1) : '0' },
      ]

      stats.forEach((stat, i) => {
        const x = margin + i * (boxW + 5)
        doc.roundedRect(x, y, boxW, 22, 3, 3, 'F')
        doc.setFontSize(18)
        doc.setTextColor(30)
        doc.text(stat.value, x + boxW / 2, y + 11, { align: 'center' })
        doc.setFontSize(8)
        doc.setTextColor(130)
        doc.text(stat.label.toUpperCase(), x + boxW / 2, y + 18, { align: 'center' })
      })

      y += 32

      // Divider
      doc.setDrawColor(230)
      doc.line(margin, y, pageW - margin, y)
      y += 8

      // Note
      doc.setFontSize(10)
      doc.setTextColor(80)
      doc.text('Session data summary. For detailed slide-by-slide analytics,', margin, y)
      y += 5
      doc.text('view the full results in the Command 360 dashboard.', margin, y)

      y += 12

      // Footer line
      doc.setDrawColor(220, 38, 38)
      doc.setLineWidth(0.5)
      doc.line(margin, y, margin + 30, y)
      y += 6
      doc.setFontSize(8)
      doc.setTextColor(150)
      doc.text('Generated by Command 360 — command360.co.uk', margin, y)

      // Save
      const filename = `${presentationTitle.replace(/[^a-zA-Z0-9]/g, '_')}_report_${sessionCode}.pdf`
      doc.save(filename)
      toast.success('PDF report downloaded')
    } catch (err) {
      console.error('PDF generation failed:', err)
      toast.error('Failed to generate PDF')
    } finally {
      setGenerating(false)
    }
  }

  return (
    <Button
      variant="outline"
      size="sm"
      onClick={handleExport}
      disabled={generating}
      className="gap-1.5 text-xs"
    >
      {generating ? (
        <Loader2 className="w-3.5 h-3.5 animate-spin" />
      ) : (
        <FileDown className="w-3.5 h-3.5" />
      )}
      {generating ? 'Generating...' : 'PDF Report'}
    </Button>
  )
}
